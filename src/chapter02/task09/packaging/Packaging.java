package chapter02.task09.packaging;

public class Packaging {

    public static void main(String[] args) {
        Integer i = 45;
        System.out.println(i);
        int ni = i;
        System.out.println(ni + 2);

        Character ch = 'F';
        System.out.println(ch);
        char nch = ch;
        System.out.println(nch);
    }
}
